import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { JhiDataUtils } from 'ng-jhipster';

import { IFragment } from 'app/shared/model/bonContentService/fragment.model';

@Component({
  selector: 'jhi-fragment-detail',
  templateUrl: './fragment-detail.component.html',
})
export class FragmentDetailComponent implements OnInit {
  fragment: IFragment | null = null;

  constructor(protected dataUtils: JhiDataUtils, protected activatedRoute: ActivatedRoute) {}

  ngOnInit(): void {
    this.activatedRoute.data.subscribe(({ fragment }) => (this.fragment = fragment));
  }

  byteSize(base64String: string): string {
    return this.dataUtils.byteSize(base64String);
  }

  openFile(contentType = '', base64String: string): void {
    this.dataUtils.openFile(contentType, base64String);
  }

  previousState(): void {
    window.history.back();
  }
}
